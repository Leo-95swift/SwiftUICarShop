/// Структура, представляющая автомобиль.
struct Car {
    /// Модель автомобиля.
    let carModel: String
    /// Имя изображения автомобиля, используемое для загрузки соответствующего изображения из ресурсов.
    let carImageName: String
    /// Цена автомобиля в долларах.
    let price: Int
}

